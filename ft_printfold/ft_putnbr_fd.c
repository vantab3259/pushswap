#include "ft_printf.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n <= -1)
	{
		n *= -1;
		c = '-';
		write(fd, &c, 1);
	}
	if (n <= 9)
	{
		c = n + '0';
		write(fd, &c, 1);
	}
	if (n >= 10)
	{
		c = n % 10 + '0';
		n = n / 10;
		ft_putnbr_fd (n, fd);
		write(fd, &c, 1);
	}
}
